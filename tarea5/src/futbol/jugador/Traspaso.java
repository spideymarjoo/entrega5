package futbol.jugador;

/**
 * Enumeración que representa las diferentes posiciones que un jugador de fútbol puede ocupar en el campo.
 */

public enum Traspaso {
    SIN_SOLICITAR,
    SOLICITADO,
    APROBADO_ENTRENADOR,
    APROBADO_PRESIDENTE,
    RECHAZADO_ENTRENADOR,
    RECHAZADO_PRESIDENTE;
}
